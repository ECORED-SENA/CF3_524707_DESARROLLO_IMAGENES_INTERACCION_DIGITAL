          p(numero="1906" titulo="Fases humorísticas de caras divertidas - Stuart Blackton") Considerada una de las primeras animaciones registradas en celuloide. Blackton utilizó un tablero y tiza para dibujar rostros que se borraban y redibujaban levemente desplazados en cada fotograma. Esta técnica generaba la ilusión de movimiento mediante la sucesión de imágenes estáticas.

          p(numero="Años 20" titulo="Felix the Cat y Gertie the Dinosaur") Estas producciones introdujeron una trama estructurada, narrativa lógica y personajes con personalidad definida. Además, incorporaron el uso sistemático de fotogramas clave (keyframes), consolidando principios técnicos fundamentales en la animación tradicional.

          p(numero="1928" titulo="Steamboat Willie – Walt Disney y Ub Iwerks") Cortometraje considerado pionero en la sincronización total entre sonido y acción en pantalla. Representa el debut oficial de Mickey Mouse y marca un avance determinante en la integración técnica del audio con la animación.

          p(numero="1930–1969" titulo="Merrie Melodies – Warner Brothers") Serie de cortometrajes animados con sonido y color que consolidaron la producción masiva de dibujos animados. Junto con Walt Disney Studios, establecieron el modelo industrial de animación cinematográfica.

          p(numero="1937" titulo="Blancanieves y los siete enanitos – Walt Disney Studios") Primer largometraje animado producido por este estudio. Constituye una innovación técnica y artística al demostrar que la animación podía sostener una narrativa extensa con profundidad emocional y calidad técnica.

          p(numero="1988" titulo="¿Quién engañó a Roger Rabbit?") Primer largometraje que combinó de manera integral actores reales con personajes animados dentro de una misma narrativa, integrando técnicas de composición y efectos especiales avanzados para la época.

          p(numero="1995" titulo="Toy Story – Pixar Animation Studios") Primera película animada realizada en su totalidad mediante medios digitales. Marcó el inicio del predominio de la animación 3D generada por computador en la industria cinematográfica.

          p(numero="Siglo XXI" titulo="Animación digital realista") La animación 3D orienta su desarrollo hacia la reproducción precisa de dinámicas físicas del mundo real. Cada nueva producción implica avances en software, hardware, simulación de partículas, iluminación y complejidad narrativa.

          p(numero="Actualidad" titulo="Inteligencia Artificial (IA)") La incorporación de algoritmos de aprendizaje automático permite automatizar procesos como interpolación de movimiento, generación de escenarios, optimización de renderizado y creación asistida de personajes, ampliando las posibilidades creativas y técnicas.
    
    
